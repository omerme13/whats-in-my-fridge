class Product {
    constructor(id, name, label, expiryDate, quantity, photo, toBuy) {
        this.id = id,
        this.name = name;
        this.label = label;
        this.expiryDate = expiryDate;
        this.quantity = quantity;
        this.photo = photo;
        this.toBuy = toBuy;
    }
}

export default Product;